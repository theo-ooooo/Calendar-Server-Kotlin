    fun generateNickname(names: List<String>): String {
        val nameSet = names.toSet()
        val randomAdjective = adjectives.random()
        val randomAnimal = animals.random()

        var index = 0
        var candidate: String

        do {
            candidate = if (index == 0) "$randomAdjective $randomAnimal"
            else "$randomAnimal, $randomAnimal #$index"
        } while (candidate in nameSet)
        return candidate
    }
